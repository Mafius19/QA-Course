import java.util.Scanner;

public class PractiseAplication {
    public static void main(String args[]) {
        int i;
        int acc = 0;
        for (i = 1; i <= 10; i++){
            acc = acc + i;
        }
        System.out.println("La suma de los 10 primeros numeros naturales es: " + acc);
    }
}
